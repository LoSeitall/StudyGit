package com.oracle.sjgl.util;

public class LimitNum {
	private int size;
	private int limitnum;
	
	
	
	public LimitNum() {
		
	}
	public LimitNum(int size, int limitnum) {
		this.size = size;
		this.limitnum = limitnum;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getLimitnum() {
		return limitnum;
	}
	public void setLimitnum(int limitnum) {
		this.limitnum = limitnum;
	}
	
	
	
}
